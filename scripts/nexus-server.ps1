switch ($Action) {
    'Help' { Show-Help }
    'Configure' {
        Write-Step 'Creating a new server environment'
        New-EnvironmentFile
    }
    'Initialize' {
        Assert-Prerequisites
        Write-Step 'Creating a new server environment'
        New-EnvironmentFile
        Invoke-Deploy
    }
    'Deploy' { Invoke-Deploy }
    'Validate' { Invoke-Validation }
    'Update' {
        Assert-Prerequisites
        Invoke-SafeGitUpdate
        Invoke-Deploy
    }
    'Start' {
        Assert-Prerequisites
        Assert-EnvironmentFile
        Invoke-Compose -Arguments @('up', '-d')
        Invoke-Compose -Arguments @('ps', '-a')
    }
    'Stop' {
        Assert-Prerequisites
        Invoke-Compose -Arguments @('stop')
        Invoke-Compose -Arguments @('ps', '-a')
    }
    'Restart' {
        Assert-Prerequisites
        Assert-EnvironmentFile
        Invoke-Compose -Arguments @('restart')
        Invoke-Compose -Arguments @('ps', '-a')
    }
    'Status' { Show-Status }
    'Diagnose' { Invoke-Diagnose }
    'Backup' {
        Assert-Prerequisites
        Assert-EnvironmentFile
        & powershell.exe -NoProfile -ExecutionPolicy Bypass -File (Join-Path $PSScriptRoot 'backup-postgres.ps1')
        if ($LASTEXITCODE -ne 0) { throw 'PostgreSQL backup failed.' }
    }
    'RepairDatabase' {
        Assert-Prerequisites
        Assert-EnvironmentFile
        & powershell.exe -NoProfile -ExecutionPolicy Bypass `
            -File (Join-Path $PSScriptRoot 'repair-synapse-collation.ps1') `
            -ConfirmRepair -StartServices
        if ($LASTEXITCODE -ne 0) { throw 'Synapse database repair failed.' }
    }
}
