    [IO.File]::WriteAllText($envPath, ($content.Trim() + [Environment]::NewLine), (New-Object Text.UTF8Encoding($false)))
    Write-Host ".env created: $envPath"
}

function Read-EnvironmentFile {
    if (-not (Test-Path -LiteralPath $envPath)) {
        throw '.env is missing. Run Initialize first.'
    }
    $values = @{}
    foreach ($line in Get-Content -LiteralPath $envPath) {
        $trimmed = $line.Trim()
        if (-not $trimmed -or $trimmed.StartsWith('#')) { continue }
        $separator = $trimmed.IndexOf('=')
        if ($separator -lt 1) { throw "Invalid .env line: $line" }
        $key = $trimmed.Substring(0, $separator).Trim()
        $value = $trimmed.Substring($separator + 1)
        if ($values.ContainsKey($key)) { throw "Duplicate .env key: $key" }
        $values[$key] = $value
    }
    return $values
}

function Assert-EnvironmentFile {
    $values = Read-EnvironmentFile
    $required = @(
        'POSTGRES_USER', 'POSTGRES_PASSWORD', 'POSTGRES_DB',
        'SYNAPSE_POSTGRES_USER', 'SYNAPSE_POSTGRES_PASSWORD', 'SYNAPSE_POSTGRES_DB',
        'CORE_API_SECRET_KEY', 'NEXUS_DOMAIN', 'NEXUS_ACME_EMAIL',
        'MATRIX_SERVER_NAME', 'MATRIX_REGISTRATION_SHARED_SECRET',
        'MATRIX_MACAROON_SECRET_KEY', 'MATRIX_FORM_SECRET',
        'OLLAMA_BASE_URL', 'OLLAMA_API_KEY', 'PLUGIN_SANDBOX_API_KEY',
        'TURN_DOMAIN', 'TURN_EXTERNAL_IP', 'TURN_AUTH_SECRET'
    )
    foreach ($key in $required) {
        if (-not $values.ContainsKey($key) -or [string]::IsNullOrWhiteSpace($values[$key])) {
            throw ".env key is missing: $key"
        }
        if ($values[$key] -match 'replace-with-|<[^>]+>') {
            throw ".env still contains a placeholder: $key"
        }
    }
    if ($values['MATRIX_SERVER_NAME'] -match '://|/') {
        throw 'MATRIX_SERVER_NAME must not contain protocol or path.'
    }
    foreach ($secretKey in @(
        'POSTGRES_PASSWORD', 'SYNAPSE_POSTGRES_PASSWORD', 'CORE_API_SECRET_KEY',
        'MATRIX_REGISTRATION_SHARED_SECRET', 'MATRIX_MACAROON_SECRET_KEY',
        'MATRIX_FORM_SECRET', 'OLLAMA_API_KEY', 'PLUGIN_SANDBOX_API_KEY',
        'TURN_AUTH_SECRET'
    )) {
        if ($values[$secretKey].Length -lt 20) {
            throw ".env secret is too short: $secretKey"
        }
    }
}

function Get-SynapseLocale {
    $json = Invoke-Compose -Arguments @('config', '--format', 'json') -Capture
    $config = $json | ConvertFrom-Json
    $adminUser = [string]$config.services.postgres.environment.POSTGRES_USER
    $maintenanceDatabase = [string]$config.services.postgres.environment.POSTGRES_DB
    $synapseDatabase = [string]$config.services.postgres.environment.SYNAPSE_POSTGRES_DB
    $escapedDatabase = $synapseDatabase.Replace("'", "''")
    $sql = "SELECT datcollate || '|' || datctype FROM pg_database WHERE datname = '$escapedDatabase'"
    $output = Invoke-Compose -Arguments @(
        'exec', '-T', 'postgres', 'psql',
        "--username=$adminUser",
        "--dbname=$maintenanceDatabase",
        '--tuples-only',
        '--no-align',
        "--command=$sql"
    ) -Capture
    return $output.Trim()
}

function Test-AiGateway {
    $values = Read-EnvironmentFile
    Write-Step 'Testing AI Gateway connectivity and authentication'
    & (Join-Path $PSScriptRoot 'test-ai-gateway-tailscale.ps1') `
        -GatewayBaseUrl $values['OLLAMA_BASE_URL'] `
        -ApiKey $values['OLLAMA_API_KEY']
    if ($LASTEXITCODE -ne 0) { throw 'AI Gateway test failed.' }
}

function Test-PublicEndpoints {
    $values = Read-EnvironmentFile
    $publicUrl = if ($values.ContainsKey('NEXUS_PUBLIC_URL')) {
        $values['NEXUS_PUBLIC_URL'].TrimEnd('/')
    }
    else {
        $port = if ($values.ContainsKey('NEXUS_HTTPS_PORT')) { [int]$values['NEXUS_HTTPS_PORT'] } else { 443 }
        Get-PublicUrl -HostName $values['NEXUS_DOMAIN'] -Port $port
    }
    if (-not (Get-Command curl.exe -ErrorAction SilentlyContinue)) {
        Write-Warning "curl.exe not found; public endpoint checks were skipped: $publicUrl"
        return
    }

    Write-Step "Waiting for public health endpoint: $publicUrl"
    $healthContent = $null
    for ($attempt = 1; $attempt -le 24; $attempt++) {
        $healthContent = & curl.exe --fail --silent --show-error --insecure --max-time 8 "$publicUrl/healthz" 2>$null
        if ($LASTEXITCODE -eq 0 -and ($healthContent | Out-String).Trim() -eq 'ok') { break }
        if ($attempt -eq 24) {
            throw "Public health endpoint did not become ready: $publicUrl/healthz"
        }
        Start-Sleep -Seconds 5
    }
    Write-Host '[OK] reverse proxy health'

    & curl.exe --fail --silent --show-error --insecure --max-time 10 "$publicUrl/api/health" | Out-Null
    if ($LASTEXITCODE -ne 0) { throw 'Backend public health check failed.' }
    Write-Host '[OK] backend health'

    $matrix = & curl.exe --fail --silent --show-error --insecure --max-time 10 "$publicUrl/_matrix/client/versions"
    if ($LASTEXITCODE -ne 0 -or ($matrix | Out-String) -notmatch '"versions"') {
        throw 'Matrix public health check failed.'
    }
    Write-Host '[OK] Matrix client endpoint'
}

function Invoke-Deploy {
    Assert-Prerequisites
    Assert-EnvironmentFile
    Write-Step 'Validating Docker Compose configuration'
    Invoke-Compose -Arguments @('config', '--quiet') | Out-Null
    Test-AiGateway

    if (-not $SkipBuild) {
        Write-Step 'Building service images'
        Invoke-Compose -Arguments @('build')
    }

    Write-Step 'Starting PostgreSQL and reconciling roles/databases'
    Invoke-Compose -Arguments @('up', '-d', 'postgres')
    Invoke-Compose -Arguments @('run', '--rm', 'postgres-bootstrap')

    $locale = Get-SynapseLocale
    if (-not $locale) {
        throw 'Synapse database locale could not be detected after postgres-bootstrap.'
    }
    if ($locale -ne 'C|C') {
        Write-Step "Repairing Synapse locale ($locale -> C|C) with backup"
        & powershell.exe -NoProfile -ExecutionPolicy Bypass -File (Join-Path $PSScriptRoot 'repair-synapse-collation.ps1') -ConfirmRepair
        if ($LASTEXITCODE -ne 0) { throw 'Synapse locale repair failed.' }
    }

    Write-Step 'Applying database migrations'
    Invoke-Compose -Arguments @('run', '--rm', 'migrate')

    Write-Step 'Starting the complete stack'
    Invoke-Compose -Arguments @('up', '-d')
    Invoke-Compose -Arguments @('ps', '-a')
    Test-PublicEndpoints
}

function Invoke-SafeGitUpdate {
    $git = Get-GitExecutable
    $stamp = (Get-Date).ToUniversalTime().ToString("yyyyMMdd-HHmmss'Z'")
    $dirty = (& $git status --porcelain --untracked-files=no | Out-String).Trim()
    if ($LASTEXITCODE -ne 0) { throw 'git status failed.' }
    if ($dirty) {
        Write-Step 'Saving tracked server changes before update'
        & $git stash push -m "nexus-server-auto-$stamp"
        if ($LASTEXITCODE -ne 0) { throw 'Local tracked changes could not be stashed.' }
        Write-Host "Tracked changes were preserved in git stash: nexus-server-auto-$stamp"
    }
    Write-Step 'Pulling origin/main with fast-forward only'
    & $git pull --ff-only origin main
    if ($LASTEXITCODE -ne 0) {
        throw 'git pull failed. Any automatic stash was preserved and was not popped.'
    }
}

function Show-Status {
    Assert-Prerequisites
    Assert-EnvironmentFile
    Invoke-Compose -Arguments @('ps', '-a')
}

function Invoke-Validation {
    Assert-Prerequisites
    Assert-EnvironmentFile
    Write-Step 'Validating Docker Compose configuration'
    Invoke-Compose -Arguments @('config', '--quiet') | Out-Null
    Test-AiGateway
    Write-Host '[OK] environment, Compose and AI Gateway validation completed'
}

function Invoke-Diagnose {
    Show-Status
    Write-Step 'Recent service logs'
    Invoke-Compose -Arguments @(
        'logs', '--tail', '120',
        'postgres', 'postgres-bootstrap', 'migrate', 'matrix',
        'backend', 'ai-worker', 'frontend', 'reverse-proxy'
    )
}

