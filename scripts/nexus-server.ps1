[CmdletBinding()]
param(
    [ValidateSet(
        'Help', 'Configure', 'Initialize', 'Validate', 'Deploy', 'Update',
        'Start', 'Stop', 'Restart', 'Status', 'Diagnose', 'Backup', 'RepairDatabase'
    )]
    [string]$Action = 'Help',
    [string]$ServerAddress,
    [string]$AcmeEmail,
    [string]$AiGatewayUrl,
    [string]$AiGatewayApiKey,
    [string]$TurnExternalAddress,
    [ValidateRange(1, 65535)]
    [int]$HttpPort = 8080,
    [ValidateRange(1, 65535)]
    [int]$HttpsPort = 8443,
    [switch]$SkipBuild
)

$ErrorActionPreference = 'Stop'
$repoRoot = (Resolve-Path (Join-Path $PSScriptRoot '..')).Path
Set-Location $repoRoot
$envPath = Join-Path $repoRoot '.env'

function Write-Step {
    param([string]$Message)
    Write-Host "`n==> $Message" -ForegroundColor Cyan
}

function Invoke-Checked {
    param(
        [Parameter(Mandatory = $true)][string]$FilePath,
        [Parameter(Mandatory = $true)][string[]]$Arguments,
        [switch]$Capture
    )

    $output = & $FilePath @Arguments
    if ($LASTEXITCODE -ne 0) {
        throw "Command failed: $FilePath $($Arguments -join ' ') (exit=$LASTEXITCODE)"
    }
    if ($Capture) {
        return (($output | Out-String).Trim())
    }
}

function Invoke-Compose {
    param([Parameter(Mandatory = $true)][string[]]$Arguments, [switch]$Capture)
    return Invoke-Checked -FilePath 'docker' -Arguments (@('compose') + $Arguments) -Capture:$Capture
}

function Get-GitExecutable {
    $git = Get-Command git -ErrorAction SilentlyContinue
    if ($git) { return $git.Source }

    $candidates = @(
        "$env:ProgramFiles\Git\cmd\git.exe",
        "${env:ProgramFiles(x86)}\Git\cmd\git.exe",
        "$env:LOCALAPPDATA\Programs\Git\cmd\git.exe"
    )
    foreach ($candidate in $candidates) {
        if ($candidate -and (Test-Path -LiteralPath $candidate)) { return $candidate }
    }
    throw 'Git was not found. Install Git for Windows first.'
}

function Assert-Prerequisites {
    if (-not (Get-Command docker -ErrorAction SilentlyContinue)) {
        throw 'Docker was not found. Install/start Docker Desktop first.'
    }
    Invoke-Checked -FilePath 'docker' -Arguments @('compose', 'version') | Out-Null
    [void](Get-GitExecutable)
}

function New-RandomSecret {
    param([int]$ByteCount = 36)
    $bytes = New-Object byte[] $ByteCount
    $rng = [Security.Cryptography.RandomNumberGenerator]::Create()
    try { $rng.GetBytes($bytes) } finally { $rng.Dispose() }
    return [Convert]::ToBase64String($bytes).TrimEnd('=').Replace('+', '-').Replace('/', '_')
}

function Read-SecretValue {
    param([string]$Prompt)
    $secure = Read-Host $Prompt -AsSecureString
    $pointer = [Runtime.InteropServices.Marshal]::SecureStringToBSTR($secure)
    try {
        return [Runtime.InteropServices.Marshal]::PtrToStringBSTR($pointer)
    }
    finally {
        [Runtime.InteropServices.Marshal]::ZeroFreeBSTR($pointer)
    }
}

function Assert-ServerAddress {
    param([string]$Value)
    if ([string]::IsNullOrWhiteSpace($Value) -or $Value -match '://|/|\s') {
        throw 'ServerAddress must be an IP address or DNS name without protocol/path.'
    }
}

function Get-PublicUrl {
    param([string]$HostName, [int]$Port)
    if ($Port -eq 443) { return "https://$HostName" }
    return "https://${HostName}:$Port"
}

function New-EnvironmentFile {
    if (Test-Path -LiteralPath $envPath) {
        throw ".env already exists and was not overwritten: $envPath"
    }
    Assert-ServerAddress $ServerAddress
    if ([string]::IsNullOrWhiteSpace($AcmeEmail) -or $AcmeEmail -notmatch '@') {
        throw 'AcmeEmail is required.'
    }
    if ([string]::IsNullOrWhiteSpace($AiGatewayUrl) -or $AiGatewayUrl -notmatch '^https?://') {
        throw 'AiGatewayUrl must start with http:// or https://.'
    }
    if ([string]::IsNullOrWhiteSpace($AiGatewayApiKey)) {
        $AiGatewayApiKey = $env:NEXUS_SETUP_AI_GATEWAY_API_KEY
    }
    if ([string]::IsNullOrWhiteSpace($AiGatewayApiKey)) {
        $AiGatewayApiKey = Read-SecretValue 'Existing AI Gateway API key'
    }
    if ([string]::IsNullOrWhiteSpace($AiGatewayApiKey) -or $AiGatewayApiKey.Length -lt 32) {
        throw 'AiGatewayApiKey must contain the existing AI Gateway key (at least 32 characters).'
    }
    if ([string]::IsNullOrWhiteSpace($TurnExternalAddress)) {
        $TurnExternalAddress = $ServerAddress
    }

    $publicUrl = Get-PublicUrl -HostName $ServerAddress -Port $HttpsPort
    $matrixAuthority = if ($HttpsPort -eq 443) { "${ServerAddress}:443" } else { "${ServerAddress}:$HttpsPort" }
    $content = @"
# Generated by scripts/nexus-server.ps1. Do not commit this file.
POSTGRES_ADMIN_USER=postgres
POSTGRES_ADMIN_PASSWORD=$(New-RandomSecret)
POSTGRES_USER=nexus
POSTGRES_PASSWORD=$(New-RandomSecret)
POSTGRES_DB=nexus
POSTGRES_HOST=postgres
POSTGRES_PORT=5432

SYNAPSE_POSTGRES_USER=synapse
SYNAPSE_POSTGRES_PASSWORD=$(New-RandomSecret)
SYNAPSE_POSTGRES_DB=synapse

CORE_API_SECRET_KEY=$(New-RandomSecret)

NEXUS_DOMAIN=$ServerAddress
NEXUS_ACME_EMAIL=$AcmeEmail
NEXUS_HTTP_PORT=$HttpPort
NEXUS_HTTPS_PORT=$HttpsPort
NEXUS_PUBLIC_URL=$publicUrl
NEXUS_MATRIX_SERVER_AUTHORITY=$matrixAuthority

MATRIX_SERVER_NAME=$ServerAddress
MATRIX_HOMESERVER_URL=$publicUrl
MATRIX_REGISTRATION_SHARED_SECRET=$(New-RandomSecret)
MATRIX_MACAROON_SECRET_KEY=$(New-RandomSecret)
MATRIX_FORM_SECRET=$(New-RandomSecret)

OLLAMA_BASE_URL=$AiGatewayUrl
OLLAMA_API_KEY=$AiGatewayApiKey
OLLAMA_DEFAULT_MODEL=qwen2.5:7b
OLLAMA_CONNECT_TIMEOUT_SECONDS=5
OLLAMA_READ_TIMEOUT_SECONDS=120
OLLAMA_MAX_RETRIES=2
OLLAMA_RETRY_BACKOFF_SECONDS=0.5
OLLAMA_MODEL_CACHE_SECONDS=30

AI_WORKER_POLL_SECONDS=0.5
AI_WORKER_LEASE_SECONDS=300
AI_WORKER_MAX_ATTEMPTS=3
AI_WORKER_RETRY_BACKOFF_SECONDS=2.0
AI_MAX_PENDING_JOBS_PER_USER=20
AI_CONTEXT_TOKEN_BUDGET=3072
AI_MAX_OUTPUT_TOKENS=1024
AI_STREAM_POLL_SECONDS=0.15

PLUGIN_EXECUTION_MODE=sandbox
PLUGIN_SANDBOX_URL=http://plugin-sandbox:8091
PLUGIN_SANDBOX_API_KEY=$(New-RandomSecret)
PLUGIN_SANDBOX_TIMEOUT_SECONDS=10
PLUGIN_SANDBOX_MAX_PAYLOAD_BYTES=65536
PLUGIN_SANDBOX_MAX_OUTPUT_BYTES=65536

TURN_DOMAIN=$ServerAddress
TURN_PORT=3478
